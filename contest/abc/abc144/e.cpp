void solve() {
    ll n, k;
    cin >> n >> k;
    vl a(n), f(n);
    cin >> a >> f;
    sort(all(a), greater<>()), sort(all(f));
    ll s = -1;
    ll e = *max_element(all(a)) * *max_element(all(f));
    while (e - s > 1) {
        ll m = (s + e) / 2;
        ll k_now = k;
        bool satis = true;
        rep(i, n) {
            ll res = max(0LL, a[i] - m / f[i]);
            if (k_now < res) {
                satis = false;
                break;
            }
            k_now -= res;
        }
        if (satis) e = m;
        else s = m;
    }
    cout << e << '\n';
}

